# Strategy hint
# ---------------------------------------------------------------------------


def suggest_history_strategy(
    history_secrets: int,
    non_noreply_emails: int,
    large_blobs: int,
    pre_existing_strategy: str | None,
) -> str:
    """Return 'nuke' | 'filter-repo' | 'preserve' based on findings."""
    if pre_existing_strategy:
        return pre_existing_strategy
    # Widespread contamination → nuke
    if history_secrets > 20:
        return "nuke"
    # Localized issues → filter-repo
    if history_secrets > 0 or non_noreply_emails > 0 or large_blobs > 0:
        return "filter-repo"
    return "preserve"
