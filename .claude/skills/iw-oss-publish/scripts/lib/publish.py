# History-rewrite emitters
# ---------------------------------------------------------------------------


def write_nuke_script(ctx: Context, history_secrets: int, history_pii: int) -> Path:
    """Write history-rewrite-nuke.sh — re-init with a single squashed commit.

    See references/history_rewrite.md for the full decision tree.
    """
    path = ctx.iw_dir / "history-rewrite-nuke.sh"
    content = f"""#!/usr/bin/env bash
# Generated by iw-oss-publish — NUKE-AND-REINIT history rewrite.
#
# ⚠  IRREVERSIBLE. Run ONLY if:
#   - You do NOT need to preserve git history, AND
#   - All collaborators are informed and will re-clone after the rewrite, AND
#   - No PRs depend on current commit SHAs.
#
# Replaces the entire history with a single "Initial public release" commit.
# Force-pushes to origin. Tags and branches other than main are deleted remotely.

set -euo pipefail

# ---------------------------------------------------------------------------
# Sanity preamble — DO NOT remove
# ---------------------------------------------------------------------------
if [ "$(git rev-parse --abbrev-ref HEAD 2>/dev/null)" = "" ]; then
  echo "error: not in a git repository" >&2; exit 1
fi
read -p "Nuke ALL history for $(git remote get-url origin 2>/dev/null || echo '<no remote>')? [type 'yes' to confirm] " CONFIRM
[ "$CONFIRM" = "yes" ] || {{ echo "aborted"; exit 0; }}

# ---------------------------------------------------------------------------
# 1. Snapshot the pre-nuke state for private archival (bundle format)
# ---------------------------------------------------------------------------
ARCHIVE="$HOME/iw-oss-publish-archives"
mkdir -p "$ARCHIVE"
BUNDLE="$ARCHIVE/{ctx.config.get("project_name", "repo")}-pre-public-$(date +%Y%m%d-%H%M%S).bundle"
git bundle create "$BUNDLE" --all
echo "  ✓ pre-nuke archive: $BUNDLE"

# ---------------------------------------------------------------------------
# 2. Create orphan root with the current working tree
# ---------------------------------------------------------------------------
git checkout --orphan iw-public-root
git add -A
git -c commit.gpgsign=false commit -s -m "Initial public release"
git branch -M main

# ---------------------------------------------------------------------------
# 3. Delete all other local branches and tags
# ---------------------------------------------------------------------------
for br in $(git branch --format='%(refname:short)' | grep -v '^main$' || true); do
  git branch -D "$br" || true
done
for tag in $(git tag -l); do
  git tag -d "$tag" || true
done

# ---------------------------------------------------------------------------
# 4. Force-push to origin (if remote exists)
# ---------------------------------------------------------------------------
if git remote get-url origin >/dev/null 2>&1; then
  read -p "Force-push main to origin (overwrites remote history)? [type 'push' to confirm] " GO
  if [ "$GO" = "push" ]; then
    git push --force origin main
    # Delete all remote branches except main
    for br in $(git ls-remote --heads origin | awk '{{print $2}}' | sed 's#refs/heads/##' | grep -v '^main$' || true); do
      git push --delete origin "$br" || true
    done
    # Delete all remote tags
    for tag in $(git ls-remote --tags origin | awk '{{print $2}}' | sed 's#refs/tags/##' | grep -v '^.*\\^{{}}$' || true); do
      git push --delete origin "$tag" || true
    done
    echo "  ✓ origin rewritten"
  else
    echo "  · skipped remote push; run manually when ready:"
    echo "    git push --force origin main"
  fi
fi

echo ""
echo "NUKE-AND-REINIT complete."
echo ""
echo "Follow-ups:"
echo "  1. All collaborators MUST re-clone (existing clones will diverge)."
{"  2. Open a GitHub Support ticket to purge cached commit SHAs:" if history_secrets else "#  2. (skipped — no secrets found in history)"}
{"     https://support.github.com/contact — 'Sensitive data in a repository'" if history_secrets else ""}
"""
    path.write_text(content, encoding="utf-8")
    path.chmod(path.stat().st_mode | stat.S_IEXEC | stat.S_IXGRP | stat.S_IXOTH)
    return path


def write_filter_repo_script(ctx: Context) -> Path:
    """Write history-rewrite-filter-repo.sh — surgical rewrite.

    This is a TEMPLATE script — the user must add specific paths / replacements
    based on their scan findings before running.
    """
    path = ctx.iw_dir / "history-rewrite-filter-repo.sh"
    content = """#!/usr/bin/env bash
# Generated by iw-oss-publish — FILTER-REPO surgical history rewrite.
#
# ⚠  IRREVERSIBLE. Force-pushes to origin.
# ⚠  This is a TEMPLATE. Fill in the PATHS_TO_PURGE and REPLACEMENTS arrays
#    below before running.
#
# See references/history_rewrite.md for the full command reference and pitfalls.

set -euo pipefail

# ---------------------------------------------------------------------------
# PATHS_TO_PURGE — files to remove from all history
# ---------------------------------------------------------------------------
PATHS_TO_PURGE=(
  # Examples — replace with paths from your scan findings:
  # "config/.env.production"
  # "secrets/"
  # "vendor/internal-licensed/"
)

# ---------------------------------------------------------------------------
# Literal-string replacements written to a file for git filter-repo
# Format: <literal>==><replacement>   (one per line)
# ---------------------------------------------------------------------------
REPLACEMENTS_FILE="$(mktemp)"
cat > "$REPLACEMENTS_FILE" <<'EOF_REPLACE'
# Example:
# old_api_key==>REDACTED
# https://internal.wiki/==>https://example.com/docs/
EOF_REPLACE

# ---------------------------------------------------------------------------
# Mailmap — pseudonymize historical author emails
# Format: <new-name> <new-email> <old-email>  (one per line)
# ---------------------------------------------------------------------------
MAILMAP_FILE="$(mktemp)"
cat > "$MAILMAP_FILE" <<'EOF_MAILMAP'
# Example (replaces a personal email with a GitHub noreply):
# <12345+sergiog@users.noreply.github.com> <sergio@personal.com>
EOF_MAILMAP

# ---------------------------------------------------------------------------
# Execution — REQUIRES A FRESH CLONE
# ---------------------------------------------------------------------------
if ! git-filter-repo --help >/dev/null 2>&1; then
  echo "error: git-filter-repo not installed. See install_tools.sh." >&2
  exit 1
fi

read -p "Run git-filter-repo with the configuration above? [type 'yes' to confirm] " CONFIRM
[ "$CONFIRM" = "yes" ] || { echo "aborted"; exit 0; }

# Path purge
if [ ${#PATHS_TO_PURGE[@]} -gt 0 ]; then
  git-filter-repo --force \\
    $(for p in "${PATHS_TO_PURGE[@]}"; do echo --path "$p"; done) \\
    --invert-paths
fi

# Content replace
if [ -s "$REPLACEMENTS_FILE" ] && grep -v '^#' "$REPLACEMENTS_FILE" | grep -q .; then
  git-filter-repo --force --replace-text "$REPLACEMENTS_FILE"
fi

# Author rewrite
if [ -s "$MAILMAP_FILE" ] && grep -v '^#' "$MAILMAP_FILE" | grep -q .; then
  git-filter-repo --force --mailmap "$MAILMAP_FILE"
fi

echo ""
echo "FILTER-REPO complete. Inspect the rewrite with:"
echo "  git log --oneline"
echo ""
echo "When satisfied, push (force-with-lease is SAFER than --force here):"
echo "  git remote add origin <url>  # filter-repo strips remotes for safety"
echo "  git push --force origin --all"
echo "  git push --force origin --tags"
"""
    path.write_text(content, encoding="utf-8")
    path.chmod(path.stat().st_mode | stat.S_IEXEC | stat.S_IXGRP | stat.S_IXOTH)
    return path


