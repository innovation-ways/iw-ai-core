def write_publish_checklist(
    ctx: Context,
    blockers: list[Finding],
    history_strategy_suggested: str,
    history_secrets: int,
    artifacts: dict[str, Path],
) -> Path:
    cfg = ctx.config
    parsed = parse_github_remote(ctx.repo.remote_url)
    org_repo = (
        "/".join(parsed)
        if parsed
        else f"{cfg.get('company_github_org', '?')}/{cfg.get('project_name', '?')}"
    )

    lines = [
        f"# Publish Checklist — {cfg.get('project_name', '(unnamed)')}",
        "",
        f"**Target:** `{org_repo}`",
        f"**Current HEAD:** `{ctx.repo.head_sha[:12] or 'unknown'}`",
        f"**Current visibility:** `{ctx.repo.visibility}`",
        f"**License:** `{cfg.get('license', 'unset')}`",
        "",
        "---",
        "",
    ]

    if blockers:
        lines.append("## ❌ Blocked — address these BEFORE publishing")
        lines.append("")
        for f in blockers:
            lines.append(f"- [ ] **{f.id}** — {f.summary}")
            if f.remediation:
                lines.append(f"  - {f.remediation}")
        lines.append("")
        lines.append(
            "Run `iw-oss-publish make_oss` first, or address each blocker manually, "
            "then re-run `iw-oss-publish publish`."
        )
        lines.append("")
    else:
        lines.append("## ✅ Pre-flight clean")
        lines.append("")
        lines.append("No MUST-level findings. You can proceed.")
        lines.append("")

    lines.extend(
        [
            "## Step 1 — Choose history strategy",
            "",
            f"**Suggested:** `{history_strategy_suggested}` — see `references/history_rewrite.md` for the decision tree.",
            "",
            "Available strategies:",
            "",
            "- **NUKE** (`.iw/history-rewrite-nuke.sh`): fresh squashed init commit. Zero history preserved. Simplest and safest when history has no consumer value.",
            "- **FILTER-REPO** (`.iw/history-rewrite-filter-repo.sh`): surgical removal of specific files/strings/authors; preserves commit graph. Edit the script's PATHS_TO_PURGE and REPLACEMENTS arrays before running.",
            "- **PRESERVE**: publish history as-is. Only appropriate when secrets scan is clean and no internal references were committed.",
            "",
        ]
    )

    if history_secrets > 0:
        lines.append(
            f"⚠  `gitleaks` found **{history_secrets} secret finding(s) in history** — "
            "PRESERVE is NOT an option. Use NUKE or FILTER-REPO."
        )
        lines.append("")

    lines.extend(
        [
            "## Step 2 — Run the chosen history-rewrite script",
            "",
            "```bash",
            "# Pick ONE:",
            "bash .iw/history-rewrite-nuke.sh",
            "# or",
            "bash .iw/history-rewrite-filter-repo.sh   # edit the arrays first",
            "```",
            "",
            "Each script prompts before doing anything destructive.",
            "",
            "## Step 3 — Re-verify after rewrite",
            "",
            "```bash",
            "python3 .claude/skills/iw-oss-publish/scripts/scan.py",
            "```",
            "",
            "This catches any secrets that survived the rewrite. If MUST findings appear, stop.",
            "",
            "## Step 4 — Flip to public + harden settings",
            "",
            "```bash",
            "bash .iw/publish-playbook.sh",
            "```",
            "",
            "The playbook prompts before the visibility flip and branch-protection apply.",
            "",
            "## Step 5 — Post-publish manual follow-ups",
            "",
            "- [ ] Install `cncf/dco2` GitHub App on the org (if not already) → https://github.com/apps/dco",
            "- [ ] Run USPTO Trademark Search → https://tmsearch.uspto.gov/",
            "- [ ] Run WIPO Global Brand Database search → https://branddb.wipo.int/",
            "- [ ] Tag first release if none exists (`git tag -s v0.1.0 …`)",
            "- [ ] Add OpenSSF Scorecard badge to README after first run",
        ]
    )
    if history_secrets > 0:
        lines.append(
            "- [ ] **Open a GitHub Support ticket** to purge cached commit SHAs after history rewrite "
            "→ https://support.github.com/contact (category: *Sensitive data in a repository*)"
        )
    lines.append("")

    lines.extend(
        [
            "---",
            "",
            "## Artifacts",
            "",
        ]
    )
    for name, p in artifacts.items():
        lines.append(f"- `{p.relative_to(ctx.target)}` — {name}")
    lines.append("")

    path = ctx.iw_dir / "publish-checklist.md"
    path.write_text("\n".join(lines), encoding="utf-8")
    return path


