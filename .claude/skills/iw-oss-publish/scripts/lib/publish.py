"""Publish mode — emit scripts and checklist for flipping a repo to public.

Every destructive action is written to a shell script the USER runs
manually. Nothing in this module executes git-history-rewrite or
gh-repo-edit commands against live state.
"""

from __future__ import annotations

import stat
from pathlib import Path

from .context import Context
from .types import Finding

# ---------------------------------------------------------------------------
# Derive org/repo from remote URL
# ---------------------------------------------------------------------------


def parse_github_remote(remote_url: str) -> tuple[str, str] | None:
    """Extract (org, repo) from a GitHub SSH or HTTPS URL, or None."""
    if not remote_url:
        return None
    s = remote_url.strip().removesuffix(".git")
    # git@github.com:org/repo
    if s.startswith("git@github.com:"):
        s = s[len("git@github.com:") :]
    # https://github.com/org/repo
    elif "github.com/" in s:
        s = s.split("github.com/", 1)[1]
    else:
        return None
    parts = s.split("/")
    if len(parts) != 2:
        return None
    return parts[0], parts[1]


