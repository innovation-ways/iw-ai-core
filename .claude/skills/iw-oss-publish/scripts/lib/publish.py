def write_gh_playbook(ctx: Context, has_history_rewrite: bool) -> Path:
    """Write publish-playbook.sh — gh commands for flipping public + hardening."""
    path = ctx.iw_dir / "publish-playbook.sh"
    cfg = ctx.config
    parsed = parse_github_remote(ctx.repo.remote_url)
    if parsed:
        org, repo = parsed
    else:
        org = cfg.get("company_github_org", "innovation-ways")
        repo = cfg.get("project_name", ctx.target.name)

    desc = (
        cfg.get("project_description", "").strip()
        or f"{cfg.get('project_name', '')} — {cfg.get('company_brand', '')}"
    )
    homepage = cfg.get("homepage", "https://innovation-ways.com")

    topics: list[str] = sorted(ctx.ecosystems)
    if cfg.get("license"):
        topics.append(cfg["license"].lower().replace(".", "-"))
    topics = topics[:10]

    content = f"""#!/usr/bin/env bash
# Generated by iw-oss-publish on {ctx.repo.head_sha[:12] or "unknown"}
# Target: {org}/{repo}
#
# Runs gh CLI commands to flip visibility, configure settings, and enable
# GitHub-native security features.
#
# EVERY destructive step prompts for confirmation.
# Re-runnable: every command is idempotent.

set -euo pipefail
ORG="{org}"
REPO="{repo}"

if ! command -v gh >/dev/null 2>&1; then
  echo "error: gh CLI not installed" >&2; exit 1
fi
gh auth status >/dev/null 2>&1 || {{ echo "error: run 'gh auth login' first" >&2; exit 1; }}

confirm() {{
  read -p "$1 [y/N] " -n 1 -r; echo
  [[ $REPLY =~ ^[Yy]$ ]] || {{ echo "  skipped."; return 1; }}
}}

# ---------------------------------------------------------------------------
# 1. Flip to public (IRREVERSIBLE WITHOUT SUPPORT TICKET)
# ---------------------------------------------------------------------------
confirm "Flip $ORG/$REPO to PUBLIC visibility?" && \\
  gh repo edit "$ORG/$REPO" --visibility public --accept-visibility-change-consequences

# ---------------------------------------------------------------------------
# 2. Description, homepage, topics, merge settings
# ---------------------------------------------------------------------------
gh repo edit "$ORG/$REPO" \\
  --description "{desc}" \\
  --homepage "{homepage}" \\
{chr(10).join(f'  --add-topic "{t}" \\\\' for t in topics)}
  --enable-issues \\
  --enable-squash-merge \\
  --enable-merge-commit=false \\
  --delete-branch-on-merge

# ---------------------------------------------------------------------------
# 3. Branch protection on main
# ---------------------------------------------------------------------------
confirm "Apply branch protection to main?" && \\
gh api "repos/$ORG/$REPO/branches/main/protection" \\
  --method PUT \\
  --field required_status_checks='{{"strict":true,"contexts":["dco","compliance"]}}' \\
  --field enforce_admins=true \\
  --field required_pull_request_reviews='{{"required_approving_review_count":1,"require_code_owner_reviews":true,"dismiss_stale_reviews":true}}' \\
  --field allow_deletions=false \\
  --field allow_force_pushes=false \\
  --field restrictions=null

# ---------------------------------------------------------------------------
# 4. Secret scanning + push protection
# ---------------------------------------------------------------------------
gh api "repos/$ORG/$REPO" --method PATCH \\
  --field security_and_analysis='{{"secret_scanning":{{"status":"enabled"}},"secret_scanning_push_protection":{{"status":"enabled"}}}}' \\
  || echo "  (requires Code Security features — verify via the web UI if this errors)"

# ---------------------------------------------------------------------------
# 5. Dependabot alerts + security updates
# ---------------------------------------------------------------------------
gh api "repos/$ORG/$REPO/vulnerability-alerts" --method PUT
gh api "repos/$ORG/$REPO/automated-security-fixes" --method PUT

# ---------------------------------------------------------------------------
# 6. Private Vulnerability Reporting
# ---------------------------------------------------------------------------
gh api "repos/$ORG/$REPO/private-vulnerability-reporting" --method PUT \\
  || echo "  (PVR requires repo admin — verify in Settings → Security)"

# ---------------------------------------------------------------------------
# 7. Reminders (manual)
# ---------------------------------------------------------------------------
cat <<REMINDERS

MANUAL FOLLOW-UPS:

  [ ] Install cncf/dco2 GitHub App on the org if not already:
      https://github.com/apps/dco

  [ ] Run USPTO and WIPO trademark searches for "{cfg.get("project_name", "")}":
      https://tmsearch.uspto.gov/
      https://branddb.wipo.int/

  [ ] Tag first release (if you haven't already):
      git tag -s v0.1.0 -m 'Initial public release'
      git push origin v0.1.0
      gh release create v0.1.0 --generate-notes

  [ ] Add OpenSSF Scorecard badge to README (after the first scorecard run):
      https://api.securityscorecards.dev/projects/github.com/$ORG/$REPO/badge

{
        '''  [ ] IMPORTANT — open a GitHub Support ticket to purge cached commit SHAs
      (required after history rewrite): https://support.github.com/contact
      Category: "Sensitive data in a repository"
'''
        if has_history_rewrite
        else ""
    }
REMINDERS

echo ""
echo "Playbook complete. Verify settings with:"
echo "  gh repo view $ORG/$REPO --web"
"""
    path.write_text(content, encoding="utf-8")
    path.chmod(path.stat().st_mode | stat.S_IEXEC | stat.S_IXGRP | stat.S_IXOTH)
    return path


